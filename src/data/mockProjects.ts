    inch: '65"',
    factoryModel: 'OLED65G5PSA',
    salesModelSuffix: '.AKR',
    marketType: 'B2C',
    publishingChannel: 'LG.com',
    assetType: 'Hero Image',
    searchTag: 'oled, premium, 2026',
    archiveRule: '신규 등록',
