let assetSeq = 1000
function asset(p: Partial<Asset> & { fileName: string }): Asset {
  assetSeq += 1
  return {
    id: `A-${assetSeq}`,
    assetType: 'Hero Image',
    sizeKb: 820,
    ext: p.fileName.split('.').pop() ?? 'jpg',
    altText: '',
    locale: 'en-US',
    uploadedAt: '2026-05-20 10:21',
    status: 'uploaded',
    issues: [],
    ...p,
  }
}

let evtSeq = 5000
function evt(e: Omit<TimelineEvent, 'id'>): TimelineEvent {
  evtSeq += 1
  return { id: `E-${evtSeq}`, ...e }
}

